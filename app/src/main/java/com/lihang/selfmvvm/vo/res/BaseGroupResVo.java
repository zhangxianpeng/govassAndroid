package com.lihang.selfmvvm.vo.res;

public class BaseGroupResVo {
    private String msg;
    private int code;
    private GroupResVo data;

    public String getMsg() {
        return msg;
    }

    public void setMsg(String msg) {
        this.msg = msg;
    }

    public int getCode() {
        return code;
    }

    public void setCode(int code) {
        this.code = code;
    }

    public GroupResVo getData() {
        return data;
    }

    public void setData(GroupResVo data) {
        this.data = data;
    }
}
